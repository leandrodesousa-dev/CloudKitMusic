 4- Entre no site https://developer.apple.com/account/ faça o login com sua conta developer
 5- Vá parte na Certificates, Identifiers & Profiles na parte esquerda
 
 6- Clique Identifiers e aperte no +, crie um iCloud Containers
 
 7- Depois volte aqui 3, selecione o identifier que criou onde está containers 
